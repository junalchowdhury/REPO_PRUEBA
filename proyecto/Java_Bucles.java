public class Java_Bucles {
    public static void main(String[] args) {
        int i = 0;
        while(i<19){
            System.out.println(i);
            i++;
        }
        
        do{
            System.out.println(i);
            i++;
        }while(i<30);
        
        for(i=30;i<45;i++){
            System.out.println(i);
        }
        
        
        
        
    }
    
}
